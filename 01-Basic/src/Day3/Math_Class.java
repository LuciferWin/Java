package Day3;

public class Math_Class {

	public static void main(String[] args) {
		
		System.out.println("Abs Value of -7 : " + Math.abs(-7));
		System.out.println("3 power 4 : " + Math.pow(3, 4));
		System.out.println("Sqrt of 81 : " + Math.sqrt(81));
		System.out.println("Random value : " + Math.random());
		System.out.println("Min Value : " + Math.min(100, 50));
		System.out.println("Max Value : " + Math.max(99, 80));
		
		System.out.println("Round(3.5) : " + Math.round(3.5));
		System.out.println("Floor(3.8) : " + Math.floor(3.8));
		System.out.println("Celi(3.2) : " + Math.ceil(3.2));

		System.out.println("Sin(30) : " + Math.sin(30));
		System.out.println("Log10(10) : " + Math.log10(10));
		
		
	}

}
